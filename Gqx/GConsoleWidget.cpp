// SearchBox - внутренний класс окошка поиска
class GConsoleWidget::SearchBox : public QFrame
{
public:
	explicit SearchBox( GConsoleWidget *pParent ) :
		QFrame( pParent ),
		m_pEdit( 0 ),
		m_pBtnUp( 0 ),
		m_pBtnDown( 0 ),
		m_pBtnClose( 0 )
	{
		resize(300, 80);
		setAutoFillBackground( true );
		setFrameShape( QFrame::StyledPanel );

		// Layout
		QHBoxLayout *pLayout = new QHBoxLayout(this);
		pLayout->setSizeConstraint(QLayout::SetFixedSize);
		pLayout->setContentsMargins( 4, 4, 4, 4 );
		pLayout->setSpacing( 0 );

		// Строка ввода
		m_pEdit = new QLineEdit( this );
		QSize pSize = m_pEdit->minimumSizeHint();
		pSize.setWidth( pSize.width() * 4 );
		m_pEdit->setMinimumSize( pSize );
		pLayout->addWidget( m_pEdit );

		pSize = QSize( pSize.height() + 3, pSize.height() + 3 );

		// Кнопочка "Вверх"
		m_pBtnUp = new QToolButton( this );
		m_pBtnUp->setIcon( getIcon( m_btnUp_data , m_btnUp_Size ) );
		m_pBtnUp->setAutoRaise( true );
		m_pBtnUp->setMaximumSize( pSize );
		m_pBtnUp->setMinimumSize( pSize );
		m_pBtnUp->setToolTip( trUtf8( "Поиск назад (Shift+F3)" ));
		pLayout->addWidget( m_pBtnUp );

		// Кнопочка "Вниз"
		m_pBtnDown = new QToolButton( this );
		m_pBtnDown->setIcon( getIcon( m_btnDown_data , m_btnDown_Size ) );
		m_pBtnDown->setAutoRaise( true );
		m_pBtnDown->setMaximumSize( pSize );
		m_pBtnDown->setMinimumSize( pSize );
		m_pBtnDown->setToolTip( trUtf8( "Поиск вперёд (F3)" ));
		pLayout->addWidget( m_pBtnDown );

		// Промежуток небольшой
		pLayout->addItem( new QSpacerItem( 6, 20, QSizePolicy::Expanding, QSizePolicy::Minimum ) );

		// Кнопочка "Закрыть"
		m_pBtnClose = new QToolButton( this );
		m_pBtnClose->setIcon( getIcon( m_btnClose_data , m_btnClose_Size, m_btnCloseA_data , m_btnCloseA_Size ) );
		m_pBtnClose->setAutoRaise( true );
		m_pBtnClose->setMaximumSize( pSize );
		m_pBtnClose->setMinimumSize( pSize );
		m_pBtnClose->setToolTip( trUtf8( "Закрыть окно поиска (Esc)" ));
		pLayout->addWidget( m_pBtnClose );
	}

	~SearchBox()
	{
	}

public:
	QLineEdit	*m_pEdit;
	QToolButton	*m_pBtnUp,
				*m_pBtnDown,
				*m_pBtnClose;

private:
	static unsigned char const * const m_btnUp_data;
	static unsigned char const * const m_btnDown_data;
	static unsigned char const * const m_btnClose_data;
	static unsigned char const * const m_btnCloseA_data;
	static size_t const m_btnUp_Size;
	static size_t const m_btnDown_Size;
	static size_t const m_btnClose_Size;
	static size_t const m_btnCloseA_Size;

	QIcon getIcon(
		unsigned char const * const pNormalData,
		size_t cbNormalSize,
		unsigned char const * const pActiveData = 0,
		size_t cbActiveSize = 0
	)
	{
		QPixmap pxNormal;
		pxNormal.loadFromData( pNormalData, (uint) cbNormalSize );
		QIcon pIcon;
		pIcon.addPixmap( pxNormal, QIcon::Normal, QIcon::Off );
		if( 0 != pActiveData ) {
			QPixmap pxActive;
			pxActive.loadFromData( pActiveData, (uint) cbActiveSize );
			pIcon.addPixmap( pxActive, QIcon::Active, QIcon::Off );
		}
		return pIcon;
	}
};

