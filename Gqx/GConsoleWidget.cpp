// SearchBoxLayout - внутренний класс для правильного расположения окошечка поиска
class GConsoleWidget::SearchBoxLayout : public QLayout
{
public:
	SearchBoxLayout( QWidget *pParent ) : QLayout( pParent ), m_pItem( 0 ) {}
	~SearchBoxLayout()
	{
		if( 0 != m_pItem ) delete m_pItem;
	}

public:
	void addItem( QLayoutItem *pItem )
	{
		Q_ASSERT( 0 == m_pItem );	// Нельзя добавлять больше одного элемента!
		m_pItem = pItem;
	}

	QLayoutItem* itemAt( int nIndex ) const
	{
		if( 0 != nIndex ) return 0;
		return m_pItem;
	}

	QLayoutItem *takeAt( int nIndex )
	{
		Q_ASSERT( 0 == nIndex );
		Q_ASSERT( 0 != m_pItem );
		Q_UNUSED( nIndex );
		QLayoutItem *pTemp = m_pItem;
		m_pItem = 0;
		return pTemp;
	}

	int count() const
	{
		return ( 0 == m_pItem ) ? 0 : 1;
	}

	QSize sizeHint() const
	{
		if( 0 != m_pItem ) return m_pItem->sizeHint();
		return QSize( 0, 0 );
	}

	void setGeometry( const QRect &pRect )
	{
		QLayout::setGeometry( pRect );
		if( 0 == m_pItem ) return;

		QWidget *pParent = qobject_cast<QWidget*>( parent() );
		int nFromRight = ( 0 != pParent ) ? pParent->style()->pixelMetric( QStyle::PM_ScrollBarExtent ) : 16;
		nFromRight = nFromRight * 3 / 2;

		QSize szItem = m_pItem->sizeHint();

		m_pItem->setGeometry( QRect( QPoint( pRect.right() - nFromRight - szItem.width(), -2), szItem ) );
	}

private:
	QLayoutItem	*m_pItem;
};
