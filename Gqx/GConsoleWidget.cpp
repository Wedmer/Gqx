// ConsoleWidget - конструктор/деструктор
GConsoleWidget::GConsoleWidget( QWidget *pParent) :
	QTextEdit( pParent ),
	m_pForm( 0 )
{
	setStyleSheet( "QTextEdit{color:#C0C0C0;background:#000000;font-size:10pt;font-family:Courier New,Courier;}" );
	setUndoRedoEnabled( false );
	setLineWrapMode( QTextEdit::NoWrap );
	setReadOnly( true );
	setTextInteractionFlags(
		Qt::TextSelectableByMouse		|
		Qt::TextSelectableByKeyboard	|
		Qt::LinksAccessibleByMouse		|
		Qt::LinksAccessibleByKeyboard
	);

	m_pForm = new SearchBox( this );
	SearchBoxLayout *pLayout = new SearchBoxLayout( this );
	pLayout->addWidget( m_pForm );
	m_pForm->setVisible( false );

	// Горячие клавиши
	#ifdef _WIN32
		connect_sequence( QKeySequence::Find, SLOT(find()) );
		connect_sequence( QKeySequence::FindNext, SLOT(findNext()) );
		connect_sequence( QKeySequence::FindPrevious, SLOT(findPrevious()) );
	#else
		connect_sequence( QKeySequence("Ctrl+F"), SLOT(find()) );
		connect_sequence( QKeySequence("F3"), SLOT(findNext()) );
		connect_sequence( QKeySequence("Shift+F3"), SLOT(findPrevious()) );
	#endif

	connect_sequence( QKeySequence(Qt::Key_Escape), SLOT(findClose()) );

	// Элементы в окне поиска
	connect( m_pForm->m_pBtnUp, SIGNAL(clicked()), SLOT(findPrevious()) );
	connect( m_pForm->m_pBtnDown, SIGNAL(clicked()), SLOT(findNext()) );
	connect( m_pForm->m_pBtnClose, SIGNAL(clicked()), SLOT(findClose()) );

	connect( m_pForm->m_pEdit, SIGNAL(editingFinished()), SLOT(onEditFinished()) );
}
///////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////
// Слоты
void GConsoleWidget::onEditFinished()
{
	if( m_pForm->m_pEdit->hasFocus() )
		findNext();
}

void GConsoleWidget::find()
{
	m_pForm->setVisible( true );
	m_pForm->m_pEdit->setFocus();
	m_pForm->m_pEdit->selectAll();
}

void GConsoleWidget::findNext()
{
	m_pForm->setVisible( true );
	m_pForm->m_pEdit->setFocus();

	QString strToSearch = m_pForm->m_pEdit->text();
	if( strToSearch.isEmpty() )return;

	if( !QTextEdit::find( strToSearch ) )
		QApplication::beep();
}

void GConsoleWidget::findPrevious()
{
	m_pForm->setVisible( true );
	m_pForm->m_pEdit->setFocus();

	QString strToSearch = m_pForm->m_pEdit->text();
	if( strToSearch.isEmpty() )return;

	if( !QTextEdit::find( strToSearch, QTextDocument::FindBackward ) )
		QApplication::beep();
}

void GConsoleWidget::findClose()
{
	m_pForm->setVisible( false );
	setFocus();
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Методы
void GConsoleWidget::addLine( QString const& strLine, QFont::Weight nWeight, QColor pColor )
{
	QSize pSaveScroll(
		horizontalScrollBar()->sliderPosition(),
		verticalScrollBar()->sliderPosition()
	);

	bool bIsAtEnd = textCursor().atEnd();
	int nSaveAnchor = (-1);
	if( bIsAtEnd ) {
		if( textCursor().position() != textCursor().anchor() )
			nSaveAnchor = textCursor().anchor();
	}

	QTextCursor pCursor( document() );
	pCursor.movePosition( QTextCursor::End, QTextCursor::MoveAnchor );

	QTextCharFormat cf = currentCharFormat();

	cf.setFontWeight( nWeight );
	cf.setForeground( pColor );

	pCursor.setCharFormat( cf );
	pCursor.insertText( strLine + "\r\n" );

	if( bIsAtEnd ) {
		if( nSaveAnchor >= 0 ) {
			QTextCursor pTempCursor = textCursor();
			pTempCursor.setPosition( nSaveAnchor );
			setTextCursor( pTempCursor );
			moveCursor( QTextCursor::End, QTextCursor::KeepAnchor );
		} else {
			moveCursor( QTextCursor::End );
		}
		ensureCursorVisible();
	} else {
		horizontalScrollBar()->setSliderPosition( pSaveScroll.width() );
		verticalScrollBar()->setSliderPosition( pSaveScroll.height() );
	}
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Хелперы
void GConsoleWidget::connect_sequence( QKeySequence pKeys, const char *strSlot )
{
	QAction *pAction = new QAction( this );
	pAction->setShortcut( pKeys );
	addAction( pAction );
	connect( pAction, SIGNAL(triggered()), strSlot );
}

